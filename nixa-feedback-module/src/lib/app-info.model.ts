/**
 * App info.
 */
export class AppInfo {
	/**
	 * Name of the app.
	 */
	public appName: string;
	/**
	 * Name of the package of the app.
	 */
	public packageName: string;
	/**
	 * Build identifier of the app.
	 */
	public versionCode: string;
	/**
	 * Version of the app.
	 */
	public versionNumber: string;
}
